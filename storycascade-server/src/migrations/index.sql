DROP TABLE IF EXISTS novels;

CREATE TABLE IF NOT EXISTS novels (
    id SERIAL PRIMARY KEY,
    title VARCHAR(150) NOT NULL UNIQUE,
    other_titles VARCHAR(250),
    description VARCHAR(2000) NOT NULL,
    chapters INT NOT NULL,
    image_key VARCHAR(100) NOT NULL,
    state_id int NOT NULL REFERENCES states(id),
    country_id int NOT NULL REFERENCES countries(id),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE OR REPLACE FUNCTION update_timestamp()
    RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_users_timestamp
    BEFORE UPDATE ON novels
    FOR EACH ROW
    EXECUTE FUNCTION update_timestamp();DROP TABLE IF EXISTS tomes;

CREATE TABLE IF NOT EXISTS tomes (
    id SERIAL PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    novel_id INT REFERENCES novels(id)
);DROP TABLE IF EXISTS chapters;

CREATE TABLE IF NOT EXISTS chapters (
    id SERIAL PRIMARY KEY,
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    likes INT DEFAULT 0,
    tomes_id INT REFERENCES tomes(id)
);DROP TABLE IF EXISTS comments;

CREATE TABLE IF NOT EXISTS comments (
    id SERIAL PRIMARY KEY,
    content VARCHAR(1200) NOT NULL,
    user_id int NOT NULL REFERENCES users(id),
    chapter_id int NOT NULL REFERENCES chapters(id),
    parent_comment_id int REFERENCES comments(id),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE OR REPLACE FUNCTION update_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_users_timestamp
    BEFORE UPDATE ON comments
    FOR EACH ROW
    EXECUTE FUNCTION update_timestamp();DROP TABLE IF EXISTS ratings;

